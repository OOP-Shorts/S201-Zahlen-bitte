public class ZahlenBitte {

    public static void main(String[] args) {
        int[] numbers = new int[5];
        numbers[0] = 1337;
        numbers[1] = 42;
        numbers[2] = 7;
        numbers[3] = 23;
        numbers[4] = 1;
        for (int i = 0; i < numbers.length; i++) {
            System.out.println(numbers[i]);
        }
    }
}
